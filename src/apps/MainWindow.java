    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed

try {
            String old_pswd = jPasswordField1.getText();
            String new_pswd = jPasswordField2.getText();
            String cnf_pswd = jPasswordField3.getText();
            DBConnection db = new DBConnection();
            db.pstmt = db.con.prepareStatement("select password from emp_mstr where ename=?");
            db.pstmt.setString(1, ename);
            db.rst = db.pstmt.executeQuery();
            if (db.rst.next()) {
                String db_pswd = db.rst.getString(1);
                if (old_pswd.equals(db_pswd)) {
                    if (new_pswd.equals(cnf_pswd)) {
                        db.pstmt = db.con.prepareStatement("update emp_mstr set password=? where ename=?");
                        db.pstmt.setString(1, new_pswd);
                        db.pstmt.setString(2, ename);
                        int i = db.pstmt.executeUpdate();
                        if (i > 0) {
                            JOptionPane.showMessageDialog(this, "Password Updated Successfully");
                            ChangePassword.setVisible(false);
                        } else {
                            JOptionPane.showMessageDialog(this, "Error in Password Updation");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "New and Confirm Password does not Match");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Old Password Does not Match");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please Login First");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }        

// TODO add your handling code here:
    }//GEN-LAST:event_jButton14ActionPerformed

